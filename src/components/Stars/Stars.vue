<template>
  <div class="star" :class="'star-'+size">
    <span class="star-item" v-for='(star,index) in starsArr' :class='star' :key='index'></span>
  </div>
</template>

<script>
    export default {
        props: {
          score: Number, // 分数
          size: Number // 图片的尺寸
        },
      computed: {
          starsArr () {
            const scs = []
            const {score} = this
            //根据分数
            const CLASS_ON = 'on'
            const CLASS_HALF = 'half'
            const CLASS_OFF = 'off'
            /*
             score 3.2 3个on 2个off
                   3.5 3个on 1个half 1个off
             */
            // 向arr中添加n个 on 属性
            const scoreInteger = Math.floor(score) // 向下取整
            for (let i=0; i< scoreInteger; i++){
              scs.push(CLASS_ON)
            }
            //向arr中添加0个或者1个 half 属性
              // 看小数点后面的值是大于5还是小于5
            if (score*10 - scoreInteger*10 >= 5) {
              scs.push(CLASS_HALF)
            }
            // 向arr中添加n个 off 属性
              // 当总数小于5的话就添加off属性，一次次的添加，直到大于5
            while (scs.length < 5){
              scs.push(CLASS_OFF)
            }
            return scs
          }
      }
    }
</script>

